// In the trigger editor, variable dropdowns for Object fields show only the
// leaf field name after selection. This toggle rewrites the selected-value
// display to show the full ancestor path: "Object → Field → SubField".
// The dropdown list itself is left unchanged (Tulip's indentation already
// conveys hierarchy there). The original text is stashed on the element so
// the toggle reverts cleanly when disabled.
//
// How Tulip builds these dropdowns (observed in the trigger editor):
//  - The open menu is a <div role="listbox"> (or similar) with a tree of
//    <div role="option"> items. Nested items sit inside a group whose heading
//    carries the ancestor label.
//  - The closed selected-value display is a sibling of the listbox trigger
//    button, usually a <div> or <span> with a single text node.
//  - Group headings use [class*="groupHeading"] or aria-label on the group
//    container, or they are non-option siblings directly above their children.
//
// Because Tulip's DOM is generated by React with hashed class names, we
// target stable attributes (role, aria-*, data-*) wherever possible and fall
// back to structural heuristics.

(() => {
const FEATURE_ID = 'variable-full-path';
const STORAGE_KEY = 'toggles';
const STASH_ATTR = 'data-tulbelt-vfp-original';
const PATCHED_ATTR = 'data-tulbelt-vfp-patched';

// Selector for the open dropdown list container.
const LISTBOX_SELECTOR = '[role="listbox"]';
// Selector for individual options inside the listbox.
const OPTION_SELECTOR = '[role="option"]';
// Selector for the closed "selected value" display span/div inside a Select
// control. Tulip uses a few different wrappers; we look for the first
// non-empty text-bearing descendant of the select container that is NOT
// inside the open menu and NOT a hidden input.
const SELECT_CONTAINER_SELECTOR = '[class*="select__control"], [class*="Select-control"], [class*="selectContainer"]';
const SINGLE_VALUE_SELECTOR = '[class*="select__single-value"], [class*="Select-value-label"], [class*="singleValue"]';

let enabled = false;
let observer = null;

// ---------------------------------------------------------------------------
// Path reconstruction
// ---------------------------------------------------------------------------

// Given an <option> element inside an open listbox, walk up through React
// Select's group structure to collect ancestor label strings.
// React Select wraps grouped options like:
//   <div class="...group">
//     <div class="...group-heading">Label</div>
//     <div class="...group-options">
//       <div role="option">…</div>
//     </div>
//   </div>
function ancestorLabels(optionEl) {
  const labels = [];
  let node = optionEl.parentElement;
  while (node && !node.matches(LISTBOX_SELECTOR)) {
    // Look for a sibling or direct child heading element before this node.
    const heading =
      node.querySelector('[class*="group-heading"], [class*="groupHeading"], [class*="GroupHeading"]');
    if (heading) {
      const text = heading.textContent.trim();
      if (text) labels.unshift(text);
    }
    node = node.parentElement;
  }
  return labels;
}

// Build full path string for an option: ancestors + the option's own label.
function fullPath(optionEl) {
  const own = optionEl.textContent.trim();
  const ancestors = ancestorLabels(optionEl);
  if (!ancestors.length) return null; // top-level: no change needed
  return [...ancestors, own].join(' → ');
}

// ---------------------------------------------------------------------------
// Apply / revert on selected-value elements
// ---------------------------------------------------------------------------

function patchValueDisplay(displayEl, path) {
  if (displayEl.getAttribute(PATCHED_ATTR) === path) return; // already current
  if (!displayEl.hasAttribute(STASH_ATTR)) {
    displayEl.setAttribute(STASH_ATTR, displayEl.textContent);
  }
  displayEl.setAttribute(PATCHED_ATTR, path);
  displayEl.textContent = path;
}

function restoreValueDisplay(displayEl) {
  const original = displayEl.getAttribute(STASH_ATTR);
  if (original !== null) {
    displayEl.textContent = original;
    displayEl.removeAttribute(STASH_ATTR);
  }
  displayEl.removeAttribute(PATCHED_ATTR);
}

// ---------------------------------------------------------------------------
// Scan: for every visible selected-value display, see if its value matches
// a nested option in a co-located (same Select container) closed menu, and
// patch if so.
//
// We scan the whole page lazily: when a listbox closes React removes it from
// the DOM, so we can't retrospectively query it. Instead we intercept *while
// the menu is open* by watching for listbox appearance, then immediately
// record the path for the selected option (aria-selected="true"), then wait
// for the listbox to disappear and patch the now-visible single-value element.
// ---------------------------------------------------------------------------

// Map from Select container element → full path string to apply once the menu
// closes (i.e. once the single-value display is visible again).
const pendingPatch = new WeakMap();

function handleOpenMenu(listboxEl) {
  const selectedOption = listboxEl.querySelector(`${OPTION_SELECTOR}[aria-selected="true"]`);
  if (!selectedOption) return;
  const path = fullPath(selectedOption);
  if (!path) return;

  // Find the owning Select container (walk up until we find a known wrapper
  // or a reasonable ancestor that also contains a single-value display).
  let container = listboxEl.parentElement;
  for (let i = 0; i < 6 && container; i++) {
    if (
      container.querySelector(SINGLE_VALUE_SELECTOR) ||
      container.matches(SELECT_CONTAINER_SELECTOR)
    ) break;
    container = container.parentElement;
  }
  if (!container) return;
  pendingPatch.set(container, path);
}

function handleMenuClose(container) {
  const path = pendingPatch.get(container);
  if (!path) return;
  pendingPatch.delete(container);
  const display = container.querySelector(SINGLE_VALUE_SELECTOR);
  if (display) patchValueDisplay(display, path);
}

// Also patch any already-selected values on initial enable (e.g. when the
// user opens a pre-filled trigger). We can't know the path without the menu
// open, so we skip this — we only act after the user interacts. This is the
// minimal safe approach.

function restoreAll() {
  for (const el of document.querySelectorAll(`[${STASH_ATTR}]`)) {
    restoreValueDisplay(el);
  }
}

// ---------------------------------------------------------------------------
// MutationObserver
// ---------------------------------------------------------------------------

function onMutation(mutations) {
  for (const m of mutations) {
    // Nodes added: look for newly opened listboxes.
    for (const node of m.addedNodes) {
      if (!(node instanceof Element)) continue;
      if (node.matches(LISTBOX_SELECTOR)) {
        handleOpenMenu(node);
      } else {
        const inner = node.querySelector?.(LISTBOX_SELECTOR);
        if (inner) handleOpenMenu(inner);
      }
    }

    // Nodes removed: listbox disappeared → menu just closed.
    for (const node of m.removedNodes) {
      if (!(node instanceof Element)) continue;
      const wasListbox =
        node.matches?.(LISTBOX_SELECTOR) ||
        node.querySelector?.(LISTBOX_SELECTOR);
      if (!wasListbox) continue;
      // The container is the mutation target (parent of the removed listbox).
      handleMenuClose(m.target);
      // Also walk up a few levels in case React nests the portal differently.
      let p = m.target?.parentElement;
      for (let i = 0; i < 5 && p; i++) {
        handleMenuClose(p);
        p = p.parentElement;
      }
    }
  }
}

function startObserver() {
  if (observer) return;
  observer = new MutationObserver(onMutation);
  observer.observe(document.body, { childList: true, subtree: true });
}

function stopObserver() {
  observer?.disconnect();
  observer = null;
}

// ---------------------------------------------------------------------------
// Storage sync
// ---------------------------------------------------------------------------

async function syncFromStorage() {
  const { [STORAGE_KEY]: stored = {} } =
    await chrome.storage.local.get(STORAGE_KEY);
  const next = stored[FEATURE_ID] ?? true;
  if (next === enabled) return;
  enabled = next;
  if (enabled) {
    startObserver();
  } else {
    stopObserver();
    restoreAll();
  }
}

chrome.storage.onChanged.addListener((changes, area) => {
  if (area === 'local' && changes[STORAGE_KEY]) syncFromStorage();
});

syncFromStorage();
})();
